package exemplos;

public class Exemplo01 {

	public static void main(String[] args) {
		
		int i = 10;
		i = i + 1;
		i += 1;
		i ++; // acresenta SOMENTE um.
		
		System.out.println(i);
		
		int j = 10;
		j = j - 1;
		j -= 1;
		j --; // decrescenta SOMENTE um.
		
		System.out.println(j);
		
		int k = 10;
		k = k * 2;
		k *= 2;
		Math.pow(10,2); // potęncia se dá por classe.
		
		System.out.println(k);
		
		int m = 20;
		m = m/2;
		m /= 2;
		
		System.out.println(m);
	}

}
