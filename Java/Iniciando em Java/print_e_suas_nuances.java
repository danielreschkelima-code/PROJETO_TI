package exemplos;

public class print_e_suas_nuances {

	public static void main(String[] args) {
		
		//print com /n "imbutido" (por isso é escrito ln)
		System.out.println("Ola Mundo!");
		System.out.println("Hello World!");
		System.out.println("¡Hola Mundo!");
		
		System.out.print("\n");
		//print sem /n
		System.out.print("Ola ");
		System.out.print("Mundo!");
		
		// print com /n em seu interior
		System.out.println("Ola\nMundo!"); // usei println apenas para separar do interior
	}

}
